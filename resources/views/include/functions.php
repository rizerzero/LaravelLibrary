function elementSize($element){
	$appear="";
if(guessDevice()){
	switch ($element) {
    case "button": $appear="btn-sm"; break;
    case "input": $appear="input-sm"; break;
    case "colsize": $appear="col-sm-12 col-sm-offset-4"; break;
    case "colwidth": $appear="width:100%"; break;
  }
}
else{
	switch ($element) {
    case "button": $appear="btn-lg"; break;
    case "input": $appear="input-lg"; break;
    case "colsize": $appear="col-lg-12 col-lg-offset-4"; break;
    case "colwidth": $appear="width:30%"; break;
  }
}
return $appear;
}

